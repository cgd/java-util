    /**
     * Constructor
     * @param parent
     *          the parent frame
     * @param mode
     *          the mode that determines whether we're creating a project
     *          or renaming an existing project
     * @param projectManager
     *          the project manager
     * @param project
     *          the project
     */
    private CreateOrRenameProjectDialog(
            java.awt.Frame parent,
            DialogMode mode,
            ProjectManager projectManager,
            Project project)
    {
        super(parent, true);
        
        this.projectManager = projectManager;
        this.project = project;
        
        this.mode = mode;
        this.initComponents();
        if(mode == DialogMode.RENAME)
        {
            String currProjectName = this.project.getName();
            if(currProjectName == null)
            {
                currProjectName = "";
            }
            
            this.setTitle("Rename Project");
            this.projectNameTextField.setText(currProjectName);
            this.projectNameTextField.setSelectionStart(0);
            this.projectNameTextField.setSelectionEnd(currProjectName.length());
        }
        else
        {
            this.setTitle("Create Project");
        }
    }
    
