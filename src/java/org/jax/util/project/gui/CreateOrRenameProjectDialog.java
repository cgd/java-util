    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        String projectName = this.projectNameTextField.getText().trim();
        if(projectName.length() == 0)
        {
            String message =
                "Project name cannot be empty. Please enter a project name or " +
                "cancel.";
            JOptionPane.showMessageDialog(
                    this,
                    TextWrapper.wrapText(
                            message,
                            TextWrapper.DEFAULT_DIALOG_COLUMN_COUNT),
                    "Empty Project Name",
                    JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            if(this.mode == DialogMode.CREATE)
            {
                this.projectManager.createNewActiveProject().setName(projectName);
            }
            else
            {
                this.project.setName(projectName);
            }
            this.dispose();
        }
    }//GEN-LAST:event_okButtonActionPerformed
    
